const state = { lang: 'en' };
const formatNumber = value => new Intl.NumberFormat(state.lang === 'cz' ? 'cs-CZ' : 'en-US').format(value);

const header = document.querySelector('.site-header');
const progress = document.querySelector('.scroll-progress');
const navToggle = document.querySelector('.nav-toggle');
const navLinks = [...document.querySelectorAll('.main-nav a')];
const cursorGlow = document.querySelector('.cursor-glow');

window.addEventListener('scroll', () => {
  header.classList.toggle('scrolled', window.scrollY > 20);
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  progress.style.width = `${(window.scrollY / scrollable) * 100}%`;
  updateActiveNav();
});

navToggle.addEventListener('click', () => {
  const open = document.body.classList.toggle('nav-open');
  navToggle.setAttribute('aria-expanded', String(open));
});

navLinks.forEach(link => link.addEventListener('click', () => {
  document.body.classList.remove('nav-open');
  navToggle.setAttribute('aria-expanded', 'false');
}));

function updateActiveNav() {
  const sections = ['project', 'process', 'impact', 'team', 'contact'];
  const current = sections.findLast(id => {
    const el = document.getElementById(id);
    return el && el.offsetTop - 120 <= window.scrollY;
  });
  navLinks.forEach(link => link.classList.toggle('active', link.getAttribute('href') === `#${current}`));
}

if (matchMedia('(pointer: fine)').matches) {
  document.addEventListener('mousemove', event => {
    cursorGlow.style.opacity = '1';
    cursorGlow.style.left = `${event.clientX}px`;
    cursorGlow.style.top = `${event.clientY}px`;
  });
}

const reveals = document.querySelectorAll('.reveal');
const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.16 });
reveals.forEach(el => revealObserver.observe(el));

const counterObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return;
    const el = entry.target;
    const target = Number(el.dataset.target);
    const start = performance.now();
    const duration = 1300;
    function animate(now) {
      const progress = Math.min((now - start) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      el.textContent = formatNumber(Math.round(target * eased));
      if (progress < 1) requestAnimationFrame(animate);
    }
    requestAnimationFrame(animate);
    counterObserver.unobserve(el);
  });
});
document.querySelectorAll('.counter').forEach(el => counterObserver.observe(el));

const tabs = [...document.querySelectorAll('.process-tab')];
const steps = [...document.querySelectorAll('.process-step')];
tabs.forEach(tab => {
  tab.addEventListener('click', () => {
    const index = Number(tab.dataset.step);
    tabs.forEach(item => item.classList.remove('active'));
    steps.forEach(item => item.classList.remove('active'));
    tab.classList.add('active');
    steps[index].classList.add('active');
  });
});

const capacityInput = document.getElementById('capacityInput');
const hoursInput = document.getElementById('hoursInput');
const capacityValue = document.getElementById('capacityValue');
const hoursValue = document.getElementById('hoursValue');
const volumeResult = document.getElementById('volumeResult');
const energyResult = document.getElementById('energyResult');

function updateCalculator() {
  const capacity = Number(capacityInput.value);
  const hours = Number(hoursInput.value);
  const annualVolume = capacity * hours;
  const energyGWh = annualVolume * 10.6 / 1_000_000;
  capacityValue.textContent = `${formatNumber(capacity)} Nm³/h`;
  hoursValue.textContent = `${formatNumber(hours)} h`;
  volumeResult.textContent = `${(annualVolume / 1_000_000).toFixed(1)}m Nm³`;
  energyResult.textContent = `${energyGWh.toFixed(1)} GWh`;
}
capacityInput.addEventListener('input', updateCalculator);
hoursInput.addEventListener('input', updateCalculator);
updateCalculator();

const originalTexts = new Map();
document.querySelectorAll('[data-i18n]').forEach(el => originalTexts.set(el.dataset.i18n, el.textContent));

document.querySelectorAll('.lang-btn').forEach(button => {
  button.addEventListener('click', () => setLanguage(button.dataset.lang));
});

function setLanguage(lang) {
  state.lang = lang;
  document.documentElement.lang = lang === 'cz' ? 'cs' : 'en';
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.toggle('active', btn.dataset.lang === lang));
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const text = lang === 'cz' ? translations.cz[key] : originalTexts.get(key);
    if (text) el.textContent = text;
  });
  updateCalculator();
}

const copyBtn = document.querySelector('.copy-btn');
copyBtn.addEventListener('click', async () => {
  const email = copyBtn.dataset.copy;
  try {
    await navigator.clipboard.writeText(email);
    const original = copyBtn.textContent;
    copyBtn.textContent = state.lang === 'cz' ? 'Zkopírováno' : 'Copied';
    setTimeout(() => copyBtn.textContent = original, 1400);
  } catch {
    window.location.href = `mailto:${email}`;
  }
});

const form = document.getElementById('contactForm');
form.addEventListener('submit', event => {
  event.preventDefault();
  const data = new FormData(form);
  const subject = encodeURIComponent(`ZK Nextgen Power inquiry – ${data.get('topic')}`);
  const body = encodeURIComponent(
    `Name: ${data.get('name')}\nEmail: ${data.get('email')}\nTopic: ${data.get('topic')}\n\nMessage:\n${data.get('message')}`
  );
  window.location.href = `mailto:kvapil.develop@gmail.com?subject=${subject}&body=${body}`;
});

