const getText = key => translations[state.lang]?.[key] || translations.en[key] || '';
const formatNumber = value => new Intl.NumberFormat(state.lang === 'cz' ? 'cs-CZ' : 'en-US').format(value);
const formatDecimal = value => new Intl.NumberFormat(state.lang === 'cz' ? 'cs-CZ' : 'en-US', {
  minimumFractionDigits: 1,
  maximumFractionDigits: 1
}).format(value);

const header = document.querySelector('.site-header');
const progress = document.querySelector('.scroll-progress');
const navToggle = document.querySelector('.nav-toggle');
const navLinks = [...document.querySelectorAll('.main-nav a')];
const cursorGlow = document.querySelector('.cursor-glow');

window.addEventListener('scroll', () => {
  if (header) header.classList.toggle('scrolled', window.scrollY > 20);
  if (progress) {
    const scrollable = document.documentElement.scrollHeight - window.innerHeight;
    progress.style.width = scrollable > 0 ? `${(window.scrollY / scrollable) * 100}%` : '0%';
  }
  updateActiveNav();
});

if (navToggle) {
  navToggle.addEventListener('click', () => {
    const open = document.body.classList.toggle('nav-open');
    navToggle.setAttribute('aria-expanded', String(open));
    navToggle.setAttribute('aria-label', getText(open ? 'menuCloseLabel' : 'menuOpenLabel'));
  });
}

navLinks.forEach(link => link.addEventListener('click', () => {
  document.body.classList.remove('nav-open');
  if (navToggle) {
    navToggle.setAttribute('aria-expanded', 'false');
    navToggle.setAttribute('aria-label', getText('menuOpenLabel'));
  }
}));

function updateActiveNav() {
  const sections = ['project', 'process', 'impact', 'team', 'contact'];
  let current = '';
  sections.forEach(id => {
    const el = document.getElementById(id);
    if (el && el.offsetTop - 120 <= window.scrollY) current = id;
  });
  navLinks.forEach(link => link.classList.toggle('active', link.getAttribute('href') === `#${current}`));
}

if (window.matchMedia('(pointer: fine)').matches && cursorGlow) {
  document.addEventListener('mousemove', event => {
    cursorGlow.style.opacity = '1';
    cursorGlow.style.left = `${event.clientX}px`;
    cursorGlow.style.top = `${event.clientY}px`;
  });
}

const reveals = document.querySelectorAll('.reveal');
const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.16 });
reveals.forEach(el => revealObserver.observe(el));

const counterObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return;
    const el = entry.target;
    const target = Number(el.dataset.target || 0);
    const start = performance.now();
    const duration = 1300;
    function animate(now) {
      const progressValue = Math.min((now - start) / duration, 1);
      const eased = 1 - Math.pow(1 - progressValue, 3);
      el.textContent = formatNumber(Math.round(target * eased));
      if (progressValue < 1) requestAnimationFrame(animate);
    }
    requestAnimationFrame(animate);
    counterObserver.unobserve(el);
  });
});
document.querySelectorAll('.counter').forEach(el => counterObserver.observe(el));

