const capacityInput = document.getElementById('capacityInput');
const hoursInput = document.getElementById('hoursInput');
const capacityValue = document.getElementById('capacityValue');
const hoursValue = document.getElementById('hoursValue');
const volumeResult = document.getElementById('volumeResult');
const energyResult = document.getElementById('energyResult');

function updateCalculator() {
  if (!capacityInput || !hoursInput || !capacityValue || !hoursValue || !volumeResult || !energyResult) return;
  const capacity = Number(capacityInput.value);
  const hours = Number(hoursInput.value);
  const annualVolume = capacity * hours;
  const energyGWh = annualVolume * 10.6 / 1_000_000;

  capacityValue.textContent = `${formatNumber(capacity)} Nm³/h`;
  hoursValue.textContent = `${formatNumber(hours)} ${getText('hoursUnit')}`;
  volumeResult.textContent = `${formatDecimal(annualVolume / 1_000_000)} ${getText('volumeUnit')}`;
  energyResult.textContent = `${formatDecimal(energyGWh)} GWh`;
}

capacityInput?.addEventListener('input', updateCalculator);
hoursInput?.addEventListener('input', updateCalculator);

function setLanguage(lang) {
  state.lang = translations[lang] ? lang : 'en';
  localStorage.setItem('zk-lang', state.lang);
  document.documentElement.lang = state.lang === 'cz' ? 'cs' : 'en';

  const metaDescription = document.querySelector('meta[name="description"]');
  document.title = getText('metaTitle');
  if (metaDescription) metaDescription.setAttribute('content', getText('metaDescription'));

  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === state.lang;
    btn.classList.toggle('active', active);
    btn.setAttribute('aria-pressed', String(active));
  });

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const text = getText(el.dataset.i18n);
    if (text) el.textContent = text;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const text = getText(el.dataset.i18nPlaceholder);
    if (text) el.setAttribute('placeholder', text);
  });

  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const text = getText(el.dataset.i18nAlt);
    if (text) el.setAttribute('alt', text);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const text = getText(el.dataset.i18nAriaLabel);
    if (text) el.setAttribute('aria-label', text);
  });

  if (navToggle && document.body.classList.contains('nav-open')) {
    navToggle.setAttribute('aria-label', getText('menuCloseLabel'));
  }

  updateCalculator();
}

document.querySelectorAll('.lang-btn').forEach(button => {
  button.addEventListener('click', () => setLanguage(button.dataset.lang));
});

const copyBtn = document.querySelector('.copy-btn');
copyBtn?.addEventListener('click', async () => {
  const email = copyBtn.dataset.copy;
  try {
    await navigator.clipboard.writeText(email);
    copyBtn.textContent = getText('copiedEmail');
    setTimeout(() => {
      copyBtn.textContent = getText('copyEmail');
    }, 1400);
  } catch {
    window.location.href = `mailto:${email}`;
  }
});

